const DICTS: Record<string, Dict> = { de, en, fr, es, it, nl, pl, pt, uk, ar };

export function isRtl(locale: string): boolean {
  return locale === "ar";
}

export function t(locale: string, key: TKey): string {
  return DICTS[locale]?.[key] ?? de[key];
}

export function statusLabel(locale: string, status: string): string {
  if (status === "DELIVERED") return t(locale, "deliveries.statusDelivered");
  if (status === "PENDING") return t(locale, "deliveries.statusPending");
  if (status === "FAILED") return t(locale, "deliveries.statusFailed");
  return status;
}
